// ユーティリティ関数
export const getQuestionsByMode = (mode: Mode): Question[] => {
  return questions.filter(q => q.mode === mode);
};

export const getQuestionsByTrainingType = (trainingType: TrainingType): Question[] => {
  return questions.filter(q => q.trainingType === trainingType);
};

export const getQuestionsByModeAndType = (mode: Mode, trainingType: TrainingType): Question[] => {
  return questions.filter(q => q.mode === mode && q.trainingType === trainingType);
};

export const getQuestionById = (id: string): Question | undefined => {
  return questions.find(q => q.id === id);
};

export const getRandomQuestion = (mode?: Mode, trainingType?: TrainingType): Question => {
  let filtered = questions;
  if (mode) {
    filtered = filtered.filter(q => q.mode === mode);
  }
  if (trainingType) {
    filtered = filtered.filter(q => q.trainingType === trainingType);
  }
  return filtered[Math.floor(Math.random() * filtered.length)];
};
